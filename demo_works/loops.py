# while loop
i = 0
while (i < 10):
    print(f"i == {i}")
    print(f"{i} is less than 10")

    # increase i by 1 unit
    i = i + 1

# for loop
for i in range(10):
    print(i)


"""
Summary:

if you want to loop for a specific number of iterations -> for

if you are not sure how many iterations you want -> while
"""